import React, { Component } from 'react';
import {
  AppRegistry,
  StyleSheet,
  Text,
  View,
  Dimensions,
  TouchableHighlight,
} from 'react-native';

// import camera
import Camera from 'react-native-camera';

export default class camerafeature extends Component {

  clickeme(){
    //alert("was touched");
    this.camera.capture()
      .then((data) => console.log(data))
      .catch(err => console.error(err));
  }

  render() {
    return (
      <View style={styles.container}>
        <Camera
          ref={(cam) => {
            this.camera = cam;
          }}
          style={styles.preview}
          aspect={Camera.constants.Aspect.fill}>
          <TouchableHighlight onPress={this.clickeme.bind(this)}>

            <View style={{height:50, width:50,backgroundColor:"pink"}}></View>
          </TouchableHighlight>
        </Camera>
      </View>
    );
  }
}

